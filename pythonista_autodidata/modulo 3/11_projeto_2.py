from turtle import Turtle
# Inicializar uma turtle
t = Turtle()
# Definir a velocidade
t.speed(1)
# Movimentar a turtle para frente
t.forward(100)
# Rotacionar em x graus para direita
t.right(90)
t.forward(100)
# Rotacionar em x graus para esquerda
t.left(90)
t.forward(100)
# Movimentar para tr√°s
t.backward(200)
